    <h3>
      The 2018 SCBA DC-Baltimore Chapter Annual Scientific Symposium
    </h3>

    <h5>
      Li Yang, President of the SCBA DC-Baltimore Chapter
    </h5>
    <br/>

    <div class="row">
      <div class="col-md-6">
        <p class="paragraph">
         The Annual Scientific Symposium of SCBA DC-Baltimore Chapter was hold on a beautiful spring day, March 24, 2018, at the Johns Hopkins University Shady Grove Conference Center in Rockville, Maryland. The symposium was successfully organized by Dr. Mitchell Ho of National Cancer Institute. Dr. Yufei Jiang, the secretary of the SCBA DC-Baltimore Chapter, effectively managed all meeting logistics. Many volunteers from the NIH-CSSA also helped with the meeting.  
        </p>
        
        <p class="paragraph">
          The scientific program has three scientific sessions, a poster session and three career discussion panels. The scientific topics include genome biology and disease; protein biology: synthesis, modification, trafficking, structure, function and degradation; disease biology and therapies. Over fifteen investigators and professors from Johns Hopkins University, Georgetown University, George Washington University, and Medical College of Virginia, FDA, as well as NIH delivered the faculty talks. Dr. Chuxia Deng from Macau University delivered an outstanding keynote lecture. 
        </p>

        <p class="paragraph">
          Dr. Roland Owens from NIH intramural program director office gave a presentation on <a href="/public/documents/Scientific_Leadership_by_Roland_Owens.pptx">“Competing for scientific leadership positions”</a>. In addition, Emily Tai introduced the NIH Taiwanese Association to the SCBA DC-Baltimore Chapter. At the career panel discussion, a number of senior and newly independent investigators provided insightful career advice to the trainees who are interested in scientific research. There were also panels on “Career in management of research grants” and “Career in pharmaceutical industry”, which were provided by experienced grant managers and pharmaceutical company leaders. These sessions were well attended and well received.  
        </p>

        <p class="paragraph">
          For poster session, thirty abstracts were received; three were selected for short talks which were given by Dr. Wei Li (George Washington University), Jingxing Ou (NEI) and Ling Hao (NINDS). Six poster presenters were selected for Fellowship of Research Excellence award including Drs Nan Li, Dan Li, Liuya Tang, Xuexiang Du, Shuang Li and Xiaonan Zhao. The abstract selection committee consisted of more than ten faculty members from NIH, Johns Hopkins University, Georgetown University, University of Maryland and George Washington University. Finally, Dr. Ji Luo, the president-elect gave closing remarks. The SCBA DC-Baltimore Chapter symposium was a great success.
        </p>

